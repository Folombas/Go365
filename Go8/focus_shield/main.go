// Provoke –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ª—É—á–∞–π–Ω—É—é –ø—Ä–æ–≤–æ–∫–∞—Ü–∏—é —Ç—Ä–æ–ª–ª—è
func (t Troll) Provoke() string {
	if len(t.Comments) == 0 {
		return "–¢—É—Ç —Ç–∏—Ö–æ... —Å–ª–∏—à–∫–æ–º —Ç–∏—Ö–æ."
	}
	// –ò—Å–ø–æ–ª—å–∑—É–µ–º —Ç–µ–∫—É—â—É—é —Å–µ–∫—É–Ω–¥—É –¥–ª—è –ø—Å–µ–≤–¥–æ—Å–ª—É—á–∞–π–Ω–æ—Å—Ç–∏
	index := time.Now().Second() % len(t.Comments)
	return fmt.Sprintf("%s: %s", t.Name, t.Comments[index])
}

