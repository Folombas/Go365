	activities := []string{
		"–ü—å—ë—Ç —á–∞–π —Å –¥—Ä—É–∑—å—è–º–∏",
		"–ò–≥—Ä–∞–µ—Ç –≤ –Ω–∞—Å—Ç–æ–ª–∫–∏ —Å –±–ª–∏–∑–∫–∏–º–∏",
		"–ü–æ–º–æ–≥–∞–µ—Ç –≥–æ—Ç–æ–≤–∏—Ç—å —É–∂–∏–Ω",
		"–°–º–æ—Ç—Ä–∏—Ç —Å—Ç–∞—Ä—ã–µ —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏",
		"–û–±—Å—É–∂–¥–∞–µ—Ç –ø–ª–∞–Ω—ã –Ω–∞ –±—É–¥—É—â–µ–µ",
	}

	for i := 0; i < hours; i++ {
		activity := activities[i%len(activities)]
		fmt.Printf("   ‚ù§Ô∏è  –ß–∞—Å %d: %s\n", i+1, activity)
		time.Sleep(500 * time.Millisecond)
	}

	g.FamilyTime += hours
	g.Focus += 5 * hours
	if g.Focus > 100 {
		g.Focus = 100
	}
	fmt.Printf("   –°–µ–º–µ–π–Ω–æ–µ –≤—Ä–µ–º—è ‚Üë: %d —á–∞—Å–æ–≤ –∑–∞ –Ω–µ–¥–µ–ª—é\n", g.FamilyTime)
